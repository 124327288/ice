void
Slice::Gen::visitClassDefStart(const ClassDef_ptr& p)
{
    Output& out = *outputStack_.top();
    if(!out)
	return;

    out << nl << "<chapter>";
    out.inc();
    out << nl << "<title>";
    out.inc();
    out << nl << p -> scoped().substr(2);
    out.dec();
    out << nl << "</title>";

    out << nl << "<section>";
    out.inc();
    out << nl << "<title>";
    out.inc();
    out << nl << "Overview";
    out.dec();
    out << nl << "</title>";

    out.zeroIndent();
    out << nl << "<synopsis>";
    if(p -> isLocal())
	out << "local ";
    if(p -> isInterface())
	out << "interface";
    else
	out << "class";
    out << " <classname>" << p -> name() << "</classname>";
    ClassList bases = p -> bases();
    if(!bases.empty() && !bases.front() -> isInterface())
    {
	out.inc();
	out << nl << "extends";
	out.inc();
	out << nl << "<classname>" << bases.front() -> scoped().substr(2)
	    << "</classname>";
	bases.pop_front();
	out.dec();
	out.dec();
    }
    if(!bases.empty())
    {
	out.inc();
	if(p -> isInterface())
	    out << nl << "extends";
	else
	    out << nl << "implements";
	out.inc();
	ClassList::iterator q = bases.begin();
	while(q != bases.end())
	{
	    out << nl << "<classname>" << (*q) -> scoped().substr(2)
		<< "</classname>";
	    if(++q != bases.end())
		out << ',';
	}
	out.dec();
	out.dec();
    }
    out << "</synopsis>";
    out.restoreIndent();

    printComment(out, p);

    OperationList operations = p -> operations();
    operations.sort();
    if(!operations.empty())
    {
	out << nl << "<section>";
	out.inc();
	out << nl << "<title>";
	out.inc();
	out << nl << "Operation Index";
	out.dec();
	out << nl << "</title>";
	out << nl << "<variablelist>";
	out.inc();
	
	for(OperationList::iterator q = operations.begin();
	    q != operations.end();
	    ++q)
	{
	    out << nl << "<varlistentry>";
	    out.inc();
	    out << nl << "<term>";
	    out.inc();
	    out << nl << "<function>";
	    out.inc();
	    out << nl << (*q) -> name();
	    out.dec();
	    out << nl << "</function>";
	    out.dec();
	    out << nl << "</term>";
	    out << nl << "<listitem>";
	    out.inc();
	    printSummary(out, *q);
	    out.dec();
	    out << nl << "</listitem>";
	    out.dec();
	    out << nl << "</varlistentry>";
	}

	out.dec();
	out << nl << "</variablelist>";
	out.dec();
	out << nl << "</section>";
    }

    out.dec();
    out << nl << "</section>";

    for(OperationList::iterator q = operations.begin();
	q != operations.end();
	++q)
    {
	Type_ptr returnType = (*q) -> returnType();
	TypeStringList inputParams = (*q) -> inputParameters();
	TypeStringList outputParams = (*q) -> outputParameters();
	TypeList throws =  (*q) -> throws();
	
	out << nl << "<section>";
	out.inc();
	out << nl << "<title>";
	out.inc();
	out << nl << (*q) -> name();
	out.dec();
	out << nl << "</title>";
	
	out.zeroIndent();
	out << nl << "<synopsis>"
	    << (returnType ?
		typeToString(returnType) : "<type>void</type>")
	    << " <function>" << (*q) -> name() << "</function>(";
	out.inc();
	TypeStringList::iterator r = inputParams.begin();
	while(r != inputParams.end())
	{
	    out << nl << typeToString(r -> first) << " <parameter>"
		<< r -> second << "</parameter>";
	    if(++r != inputParams.end())
		out << ',';
	}
	if(!outputParams.empty())
	{
	    out << ';' << nl;
	    r = outputParams.begin();
	    while(r != outputParams.end())
	    {
		out << nl << typeToString(r -> first) << " <parameter>"
		    << r -> second << "</parameter>";
		if(++r != outputParams.end())
		    out << ',';
	    }
	}
	out << ')';
	out.dec();
	if(!throws.empty())
	{
	    out.inc();
	    out << nl << "throws";
	    out.inc();
	    TypeList::iterator r = throws.begin();
	    while(r != throws.end())
	    {
		out << nl << typeToString(*r);
		if(++r != throws.end())
		    out << ',';
	    }
	    out.dec();
	    out.dec();
	}
	out << ';';
	out << "</synopsis>";
	out.restoreIndent();
	
	printComment(out, *q);
	
	out.dec();
	out << nl << "</section>";
    }
}

